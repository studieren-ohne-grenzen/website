<template>
  <article v-if="page" class="container mx-auto">
    <nuxt-content :document="page" />
  </article>
</template>

<script>
export default {
  name: 'slug_',
  async asyncData({ $content, params, app, error }) {
    try {
      const slug = params.slug || 'landing_page'
      const page = await $content(`${app.i18n.locale}/${slug}`, 'index').fetch()
      return { page }
    } catch (err) {
      error({
        statusCode: 404,
        message: 'Page could not be found',
      })
    }
  },
  async fetch() {
    this.seo = await this.$content(`${this.$i18n.locale}`, 'seo')
      .fetch()
      .then((jsonFile) => jsonFile)
  },
  data: () => ({
    seo: {},
  }),
  head() {
    return {
      title: this.page.title
        ? `${this.page.title} – ${this.$t('sog')}`
        : this.$t('sog'),
      meta: [
        {
          hid: `description`,
          name: 'description',
          content: this.page.description,
        },
        { hid: 'og-type', property: 'og:type', content: 'website' },
        {
          hid: 'og-title',
          property: 'og:title',
          content: this.page.title
            ? `${this.page.title} – ${this.$t('sog')}`
            : this.$t('sog'),
        },
        {
          hid: 'og-desc',
          property: 'og:description',
          content: `${
            this.page.description ? this.page.description : this.seo.description
          }`,
        },
        {
          hid: 'og-image',
          property: 'og:image',
          content: `${process.env.baseUrl}${
            this.page.image ? this.page.image : this.seo.image
          }`,
        },
        {
          hid: 'og-image-width',
          property: 'og:image:width',
          content: '1200',
        },
        {
          hid: 'og-image-height',
          property: 'og:image:height',
          content: '630',
        },
        {
          hid: 'og-url',
          property: 'og:url',
          content: `${process.env.baseUrl}${this.$route.path}`,
        },
        {
          hid: 'twitter-card',
          name: 'twitter:card',
          content: 'summary',
        },
        {
          hid: 'twitter-title',
          name: 'twitter:title',
          content: this.page.title
            ? `${this.page.title} – ${this.$t('sog')}`
            : this.$t('sog'),
        },
        {
          hid: 'twitter-image',
          name: 'twitter:image',
          content: `${process.env.baseUrl}${
            this.page.image ? this.page.image : this.seo.image
          }`,
        },
        {
          hid: 'twitter-description',
          name: 'twitter:description',
          content: `${
            this.page.description ? this.page.description : this.seo.description
          }`,
        },
        {
          hid: 'twitter-url',
          name: 'twitter:url',
          content: `${process.env.baseUrl}${this.$route.path}`,
        },
      ],
    }
  },
}
</script>
